import 'package:flutter/material.dart';

/// A custom clipper for the floating action button dialog which make the
/// small triangle down the main dialog
class FloatingButtonDialogClipper extends CustomClipper<Path> {
  @override
  Path getClip(Size size) {
    final path = Path();
    path.lineTo(size.width / 2, size.height);
    path.lineTo(size.width, 0);
    path.close();
    return path;
  }

  @override
  bool shouldReclip(covariant CustomClipper<Path> oldClipper) => false;
}
